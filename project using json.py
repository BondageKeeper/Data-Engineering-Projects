def sort_user_numbers(given_phones):
    filtered_phones = []
    for phone in given_phones:
        if re.findall(r'(\b7|8)\s*\d{3}\s*\d{3}-\d{2}-\d{2}',phone):
            filtered_phones.append(f'{phone} - number is active')
        else:
            filtered_phones.append(f'{phone} - number is not active')
    return filtered_phones
