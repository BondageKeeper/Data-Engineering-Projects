def sort_secure_protocols(websites):
    filtered_websites = []
    for website in websites:
        if re.findall(r'\bhttps|ftps://[\w.-]+\b',website):
            filtered_websites.append(f'{website} - secure')
        else:
            filtered_websites.append(f'{website} - insecure')
    return filtered_websites
