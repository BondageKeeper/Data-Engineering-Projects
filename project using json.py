def postal_address(address):
    streets = []
    geolocation = []
    count = 0
    for component in address:
        for geo_data in component.values():
            if count == 0:
                geo_data = geo_data.strip(' %$#^&*@!?').upper()
                streets.append(geo_data)
                count += 1
            elif count == 1:
                geolocation.append(geo_data)
                count = 0
    pairs = list(zip(streets,geolocation))
    return pairs
